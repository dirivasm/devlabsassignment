package assignment.java.assignment1;

public class Exercise7 {

	// 7. Write a program to print the sum of first 20 natural numbers.

	public static void main(String[] args) {
		
		int firstNumber = 0;
		
		for(int i=firstNumber;i<20;i++){
			firstNumber += i;
		}
		
		System.out.println("Result of sum of first 20 natural numbers: "+firstNumber);

	}
	
}
